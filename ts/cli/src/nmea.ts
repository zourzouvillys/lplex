export function classLabel(code: number): string {
  const name = deviceClasses[code];
  return name ? `${name} (${code})` : `${code}`;
}

export function funcLabel(classCode: number, fn: number): string {
  const key = (classCode << 8) | fn;
  const name = deviceFunctions[key];
  return name ? `${name} (${fn})` : `${fn}`;
}
